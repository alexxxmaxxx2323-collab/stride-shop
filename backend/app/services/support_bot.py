def detect_intent(message: str) -> str:
    """Определить тему сообщения. Порядок проверки важен: статус заказа и
    «позвать человека» приоритетнее общих тем."""
    text = (message or "").lower()
    order = ("order_status", "delivery", "sizes", "returns", "payment",
             "human", "greeting", "thanks")
    for intent in order:
        if any(kw in text for kw in _INTENTS[intent]):
            return intent
    return "fallback"


def static_answer(intent: str) -> tuple[str, list[str]]:
    """Готовый ответ + чипы для тем, не требующих данных пользователя."""
    return _ANSWERS.get(intent, _ANSWERS["fallback"])
